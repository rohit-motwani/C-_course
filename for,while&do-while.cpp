#include<iostream>

using namespace std;

int main()
{
    for ( int i=1 ; i<=10 ; i++)
    {
        cout<<6*i<<endl;
    }
    
    int a=1;

    while (a<=10)
    {
       cout<<6*a<<endl;
        a++;
    }

    int b=1;
    do
    {
        cout<<6*b<<endl;
        b++;
    } while (b<=10);
    
    
    return 0;

}

    